	public int[] map;

	public Tile[,] FormatToTile()
	{
		Tile[,] returnValue = new Tile[y, x];
		for(int i = 0; i < y; i++)
		{
			for (int j = 0; j < x; j++)
			{
				returnValue[i, j] = (Tile)map[i * x + j];
			}
		}
		return returnValue;
	}
}

